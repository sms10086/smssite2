package com.smssite2.mgmt.staff.bean;


public class QueueMonBean {
	private String day;
	private Integer total;
	private Integer ydTotal;
	private Integer ltTotal;
	private Integer dxTotal;
	private String note;
	
	public String getDay() {
		return day;
	}
	public void setDay(String day) {
		this.day = day;
	}
	public Integer getTotal() {
		return total;
	}
	public void setTotal(Integer total) {
		this.total = total;
	}
	public Integer getYdTotal() {
		return ydTotal;
	}
	public void setYdTotal(Integer ydTotal) {
		this.ydTotal = ydTotal;
	}
	public Integer getLtTotal() {
		return ltTotal;
	}
	public void setLtTotal(Integer ltTotal) {
		this.ltTotal = ltTotal;
	}
	public Integer getDxTotal() {
		return dxTotal;
	}
	public void setDxTotal(Integer dxTotal) {
		this.dxTotal = dxTotal;
	}
	public String getNote() {
		return note;
	}
	public void setNote(String note) {
		this.note = note;
	}
	
}
